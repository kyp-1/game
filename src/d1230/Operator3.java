package d1230;

public class Operator3 {

	public static void main(String[] args) {
		/*
		 * 4칙연산, 나머지, 증감연산자
		 * 비교연산자
		 * == (같다)
		 * != (다르다)
		 * >, <
		 * >=, <=
		 */
		System.out.println(1==1);
		System.out.println(1!=1);
		System.out.println(1<1);
		System.out.println(1>1);
		System.out.println(1<=1);
		System.out.println(1>=1);
		
		
		System.out.println(1!=1 && 2==2);
		
		System.out.println(1==1 || 2!=2);
		
		
	}
}
